     private Curso armarObjetoCurso(int r){
         Curso c = new Curso();
                 
        c.setIdCurso((int) jtCursos.getModel().getValueAt(r, 0));       
        c.setAnioLectivo((int) jtCursos.getModel().getValueAt(r, 3)); 
        c.setCuatrimestre(jtCursos.getModel().getValueAt(r, 4).toString());
        c.setComision(jtCursos.getModel().getValueAt(r, 5).toString());        
        c.setEstado(jtCursos.getModel().getValueAt(r, 7).toString());
        c.setCantidadInscriptos((int) jtCursos.getModel().getValueAt(r, 8));
        c.setDesde((Date) jtCursos.getModel().getValueAt(r, 9));
        c.setHasta((Date) jtCursos.getModel().getValueAt(r, 10));
        c.setCarreraMaterias((CarreraMateria)jtCursos.getModel().getValueAt(r, 11));
        c.setProfesor((Profesor) jtCursos.getModel().getValueAt(r, 6));
         
         return c;
     }
     
     private void agregar(){
        AgregarCursoUI agregarCursoUI = new AgregarCursoUI(null,true,"AGREGAR");
        agregarCursoUI.setLocationRelativeTo(null);        
        agregarCursoUI.setVisible(true);
        
        if(agregarCursoUI.isGrabo()){
            JOptionPane.showMessageDialog(null, "Se ha añadido exitosamente");
            this.cargarGrilla();  
        }      
     }
     
     private void cerrar(){
       this.dispose();
     }
     
      private void editar(){
            //obtiene el curso seleccionado
            int r = jtCursos.getSelectedRow();
            if (r != -1) {            
                Curso c = this.armarObjetoCurso(r);
                AgregarCursoUI modificarCursoUI = new AgregarCursoUI(null, true, "EDITAR",c);
                modificarCursoUI.setLocationRelativeTo(null); 
                modificarCursoUI.setVisible(true);
                if (modificarCursoUI.isGrabo()) {
                    JOptionPane.showMessageDialog(null, "Se ha editado exitosamente el curso");
                    this.cargarGrilla();
                }
            }
            else {
                    JOptionPane.showMessageDialog(null, "Error al intentar editadar el curso ");
                }             
      }
      
      private void borrar(){
          //obtiene el curso seleccionado
            int r = jtCursos.getSelectedRow();
            if (r != -1) {
                
                CursoDAO cDAO = new CursoDAO();
                Curso c = this.armarObjetoCurso(r);
                int confirmDialog=JOptionPane.showConfirmDialog(this,"¿Esta seguro que desea eliminar el curso?" +  + JOptionPane.YES_NO_OPTION);
                    if(confirmDialog == JOptionPane.YES_OPTION){
                        
                        int existeFK = cDAO.ExisteFkACurso(c);
                        
                        if(existeFK == 0){
                            cDAO.BorrarCurso(c);
                            JOptionPane.showMessageDialog(null, "Se ha eliminado exitosamente");                        
                            this.cargarGrilla();
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "No se puede eliminar el curso existen objetos asociados al mismo");   
                        }
                                                
                    }
            }
      }
      
