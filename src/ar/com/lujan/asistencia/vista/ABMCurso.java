    private void cargarGrillaByFilters() {
        try {
            if (cmbCarrera.getSelectedIndex() == 0 && cmbMateria.getSelectedIndex() == 0) //  jtCursos.setModel(new CarreraMateriaTableModel((new CarreraMateriaDAO()).getCarreraMateria())
            {
                jtCursos.setModel(new CursoTableModel((new CursoDAO()).getCursos())
                );
            }
            if (cmbCarrera.getSelectedIndex() != 0 && cmbMateria.getSelectedIndex() == 0 && cmbProfesor.getSelectedIndex() == 0) {
                Carrera carrera = (Carrera) cmbCarrera.getSelectedItem();
                jtCursos.setModel(new CursoTableModel((new CursoDAO()).getCursosByCarrera(carrera)));
            }
            if (cmbCarrera.getSelectedIndex() == 0 && cmbMateria.getSelectedIndex() != 0 && cmbProfesor.getSelectedIndex() == 0) {
                Materia materia = (Materia) cmbMateria.getSelectedItem();
                jtCursos.setModel(new CursoTableModel((new CursoDAO()).getCursosByMateria(materia)));
            }
            if(cmbCarrera.getSelectedIndex() == 0 && cmbMateria.getSelectedIndex() == 0 && cmbProfesor.getSelectedIndex() != 0){
                Profesor profesor = (Profesor) cmbProfesor.getSelectedItem();
                jtCursos.setModel(new CursoTableModel((new CursoDAO()).getCursosByProfesor(profesor)));
            }
            if (cmbCarrera.getSelectedIndex() != 0 && cmbMateria.getSelectedIndex() != 0) {
                Carrera carrera = (Carrera) cmbCarrera.getSelectedItem();
                Materia materia = (Materia) cmbMateria.getSelectedItem();
                jtCursos.setModel(new CursoTableModel((new CursoDAO()).getCursosByCarreraMateria(carrera, materia)));
            }
        } catch (Exception e) {
            // llamaria al mensaje de error e.
        }
    }
